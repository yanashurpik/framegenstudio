function isValidEmail(e) {
  return typeof e === 'string' && /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(e) && e.length < 320;
}

async function sendEmail({ apiKey, from, to, replyTo, subject, html, scheduledAt }) {
  const payload = { from, to: [to], reply_to: replyTo, subject, html };
  if (scheduledAt) payload.scheduled_at = scheduledAt;
  const res = await fetch('https://api.resend.com/emails', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${apiKey}`,
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  });
  const text = await res.text();
  let json;
  try { json = JSON.parse(text); } catch { json = { raw: text }; }
  return { ok: res.ok, status: res.status, body: json };
}

const BUYER_SEND_DELAY_MS = 5 * 60 * 1000;

export default async function handler(req, res) {
  if (req.method === 'OPTIONS') {
    res.setHeader('Access-Control-Allow-Origin', '*');
    res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
    res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
    return res.status(204).end();
  }
  if (req.method !== 'POST') {
    return res.status(405).json({ success: false, error: 'Method not allowed' });
  }

  const RESEND_API_KEY = process.env.RESEND_API_KEY;
  const FROM = process.env.RESEND_FROM || 'FrameGen Studio <hello@framegenstudio.com>';
  const OWNER_EMAIL = process.env.OWNER_EMAIL || 'shurpik.y@gmail.com';
  const REPLY_TO = process.env.REPLY_TO || OWNER_EMAIL;

  if (!RESEND_API_KEY) {
    console.error('RESEND_API_KEY not set');
    return res.status(500).json({ success: false, error: 'Server not configured' });
  }

  let body = req.body;
  if (typeof body === 'string') {
    try { body = JSON.parse(body); } catch { body = {}; }
  }
  const email = (body && body.email) ? String(body.email).trim() : '';
  const tier = (body && body.tier) ? String(body.tier) : 'code_skill';
  const honeypot = body && body.website;

  if (honeypot) {
    return res.status(200).json({ success: true });
  }
  if (!isValidEmail(email)) {
    return res.status(400).json({ success: false, error: 'Valid email required' });
  }

  try {
    const scheduledAt = new Date(Date.now() + BUYER_SEND_DELAY_MS).toISOString();
    const buyer = await sendEmail({
      apiKey: RESEND_API_KEY,
      from: FROM,
      to: email,
      replyTo: REPLY_TO,
      subject: 'Your FrameGen dashboard — setup',
      html: SETUP_EMAIL_HTML,
      scheduledAt,
    });

    if (!buyer.ok) {
      console.error('Buyer email schedule failed:', buyer.status, buyer.body);
      return res.status(502).json({ success: false, error: 'Failed to schedule setup email' });
    }

    const notifyHtml = `
      <div style="font-family:-apple-system,BlinkMacSystemFont,sans-serif;color:#201d19;">
        <h2 style="margin:0 0 12px;">New Code+Skill signup ($20)</h2>
        <p style="margin:0 0 6px;"><strong>Email:</strong> ${email}</p>
        <p style="margin:0 0 6px;"><strong>Tier:</strong> ${tier}</p>
        <p style="margin:0 0 6px;"><strong>Time:</strong> ${new Date().toISOString()}</p>
        <p style="margin:16px 0 0;color:#5d5546;">Setup email is scheduled to send at <strong>${scheduledAt}</strong> (~5 min from now). They were directed to Wise to complete payment. If they don't pay, cancel the scheduled email in the Resend dashboard.</p>
      </div>
    `;
    await sendEmail({
      apiKey: RESEND_API_KEY,
      from: FROM,
      to: OWNER_EMAIL,
      replyTo: email,
      subject: `New $20 signup — ${email}`,
      html: notifyHtml,
    }).catch(err => console.error('Owner notify failed:', err));

    return res.status(200).json({ success: true });
  } catch (err) {
    console.error('purchase handler error:', err);
    return res.status(500).json({ success: false, error: 'Unexpected error' });
  }
}
